        <h2>Quelques conseils pour préparer un entretien d'embauche</h2>

        <p>Bonjour, vous etent a la recherche de travail ? Mais vous ne savez pas comment vous y prendre lors d'un entretien ? Vous avez peur de tout raté ?
            Je ne suis pas un pro mais je peut vous donnez quelques conseils... (cela s'applique aux hommes comme aux femmes).</p>
        <ul>
            <li> Soyez ponctuel.</li>
            <br />
            <li>Prenez conscience de votre attitude, de votre comportement physique en général :</li>
            <ul>
                <li>La manière dont vous etes assis.</li>
                <li>Evitez de serrer nerveusement les mains.</li>
                <li>Repérez vos tics (langage, gestes...).</li>
            </ul>
            <br />
            <li> Eviter de regarder votre montre.</li>
            <br />
            <li> Eviter de bailler (faite une bonne nuit avant).</li>
            <br />
            <li> Jetez votre chewing-gum.</li>
            <br />
            <li> Eviter de sentir la cigarette.</li>
            <br />
            <li> La premiere impression est toujours la meilleur, eviter de venir habiller comme un sac (et evitez de choquer votre recruteur).</li>
            <br />
            <li> Essayez d'etre le plus naturel possible.</li>
            <br />
            <li> Essayez d'etre confiant.</li>
            <br />
            <li> Restez calme , gardez votre sang froid :</li>
            <ul>
                <li>Le recruteur peut chercher a vous provoquer pour voir vos réactions.</li>
            </ul>
            <br />
            <li> Parlez fort et pas tros vite.</li>
            <br />
            <li> Regardez votre interlocuteur en face.</li>
            <br />
            <li> Souvenez- vous :</li>
            <ul>
                <li>S'asseoir quand on vous y invite.</li>
                <li>Tendre la main si le recruteur vous la tend.</li>
            </ul>
            <br />
            <li> Ayez l'air volontaire et actif.</li>
            <br />
            <li> Proposez de sortir si votre recruteur est sollicité :</li>
            <ul>
                <li>Le téléphone qui sonne ou si quelqu'un entre (en général cela n'arrive pas car vous avez pris rendez vous, mais il faut prévoir se genre de chose).</li>
            </ul>
            <br />
            <li> Soyez attentif aux paroles de votre interlocuteur.</li>
            <br />
            <li> Sachez l'écouter, évitez de lui couper la parole.</li>
            <br />
            <li> Attention prudence !</li>
            <ul>
                <li>Ne dites pas du mal de votre employeur actuel ou passés, ou de vos professeurs.</li><br />
                <li>Ne vous laissez pas entrainer a donner des informations confidentielles concernant votre ou vos employeurs passés, ils non pas a vous les demander et vous n'avez pas a leur donnez.</li><br />
                <li>Sur FACEBOOK ou autres réseaux sociaux, évitez de dire du mal de vos entreprise actuel et passés ou de vos professeurs.</li><br />
                <li>Dite vous que le monde du travail est tout petit et que l'entreprise pour laquel vous avez postuler peut racheter celle ou vous etes
                    et ainsi devenir votre patron, et il se souviendrons de vous.</li>
            </ul>
            <br />
            <li> Développez et argumentez vos réponses.</li>
            <ul>
                <li>Ne répondez pas uniquement pas monosyllabe (oui, non, peut-etre...).</li><br />
                <li>Illustrez chaque arguments que vous avancez par un exemple précis si possible, soyez claire.</li>
            </ul>
            <br />
            <li> Préparez vos questions a l'avance.</li>
            <br />
            <li> N'hesitez pas a poser des questions (pas trop).</li>
            <br />
            <li> Utilisez les informations sur l'entreprise qui sont contenues dans l'annonce ou que vous avez pu obtenir dans vos recherches.</li>
            <br />
            <li> Ne sous évaluez pas votre salaire : rappelez qu'il existe une convention collective ou votre qualification est prise en compte (Renseignez vous avant).</li>
            <br />
            <li> Evitez de vous lancer dans une discussion d'ordre religieux ou politique (Vos opinions ou vos conviction ne regarde que vous, et elles n'ont pas a etre dans une entreprise).</li>
            <br />
            <li> Essayer de faire preciser le moment de la décision avant la fin de l'entretien et ne partez pas sans savoir si vous devez recontacter l'employeur et comment.</li>
            <br />
            <li> Soyez cohérent tout au long de l'entretien.</li>
            <br />
            <li> Ne faites pas etat de démélés familiaux ou sentimentaux, s'est votre vie privées et ne regarde que vous.</li>
            <br />
            <li> Pour les femmes : il est possible que votre employeur/employeuse vous demande si vous prévoyer de faire des enfants dans les prochaines années, réfléchisez bien a votre réponse, cela peut etre cause de refus d'embauche.</li>
            <br />
        </ul>

        <p>
            Voila vous avez toute les clefs en main pour préparer un entretien d'embauche. Il faut que cela soit clair, net et précis.
        </p>

        <p>
            Bonne chance !!
        </p>
    </article>
