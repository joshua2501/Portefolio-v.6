        <h2>Magasinier</h2>

        <p><u>Position dans l'organigramme :</u></p>
        <ul>
            <li>Le magasinier reporte au directeur de la fabrication.</li>
        </ul>

        <p><u>Missions :</u></p>
        <ul>
            <li>Seconder les gestionnaires de stock sur le site d'Issy les Moulineaux.</li>
            <li>Assurer la sortie des éléments pour des différents services de VDM.</li>
            <li>Préparer les courses.</li>
            <li>Préparer les envois nationaux et internationaus.</li>
            <li>Préparer les mises à disposition.</li>
            <!--li>Assurer l'approvisionnement du laboratoire en bandes vierges.</li-->
            <li>Assurer la logistique des catalogues de certains clients.</li>
            <li>Réceptionner des marchandises.</li>
            <li>Acceuillir des coursiers ou des clients.</li>
        </ul>

        <p><u>Missions détaillées :</u></p>
        <p><i>Seconder les gestionnaires de stock sur le site d'Issy les Moulineaux.</i></p>
        <ul>
            <li>Immatriculer les éléments entrant sur le site d'Issy les Moulineaux : Renseignement de la base de donnée, avec les informations prises sur les éléments (étiquettes) et les fiches de renseignement prises sur les éléments (titres, versions audio, format, time-code, etc...).</li>
            <li>Editer des étiquettes code-barre avec des numéros FICAM.</li>
            <li>Stocker les éléments : éléments entrants et éléments fabriqués par les services de VDM. Recherches d'emplacement, rangement physique des éléments à l'emplacement désigné.</li>
            <li>Gérer les emplacements de stock pour éviter l'engorgement de celui-ci.</li>
            <li>Préparer les envois réguliers vers le stock des ULIS. Sortie informatique et physique des bandes du stock des ULIS. Rangement dans des bacs pour envoi aux ULIS.</li>
            <li>Mouvementer informatiquement toutes les bandes antrant ou sortant du stock.</li>
            <li>De façon générale, maintenir à jour la base informatique de gestion du stock VDM.</li>
        </ul>

        <p><i>Assurer la sortie des éléments pour les travaux des différents services de VDM.</i></p>
        <ul>
            <li>Veiller à ce que tous les éléments demandés par les différents services de VDM, (planning, laboratoire, DVD, sous-titrages, etc...) soient physiquement sortis du stock dans les délais les plus brefs.</li>
            <li>Faire le mouvement informatique des bandes.</li>
        </ul>

        <p><i>Préparer les courses.</i></p>
        <ul>
            <li>Rapprocher les éléments physiques des demandes informatiques de livraison.</li>
            <li>Mettre sous plis ou en carton les éléments à livrer.</li>
            <!--li>Organiser les tournées des voitures navettes de VDM. Par tranches horaires et localisation des points de livraison.</li-->
            <li>Commander les coursiers moto en fonction des sollicitations des personnels demandeurs.</li>
            <li>Editer des bons de livraison et mouvementer les éléments.</li>
        </ul>

        <p><i>Préparer les envois nationaux et internationaux.</i></p>
        <ul>
            <li>Mettre sous plis les éléments physiques à expédier.</li>
            <li>Rédiger les bordereaux d'envoi des différents services d'expedition : FEDEX, DHL, UPS, TNT, etc... de façon manuelle ou informatique.</li>
        </ul>

        <p><i>Préparer les mises à dispositions.</i></p>
        <ul>
            <li>Préparer les éléments physiques.</li>
            <li>Editer les bons de mise à disposition et mouvementer les éléments.</li>
            <li>Accueillir les personnes venant chercher les éléments.</li>
        </ul>

        <!--p><i>Assurer l'approvisionnement du laboratoire en bandes vierge.</i></p>
<ul>
<li>Veiller à maintenir à taux constant un stock de bandes vierges dans le bureau du planning.</li>
<li>Immatriculer les bandes vierges. Editer les numéros FICAM à coller sur les bandes et les boitiers.</li>
<li>Mettre dans les bandes les étiquettes VDM.</li>
<li>Mettre à disposition ces bandes dans le bureau du planning.</li>
<li>Anticiper l'immatriculation des bandes vierges en fonction des commandes en liaison avec le planning.</li>
</ul-->

        <p><i>Assurer la logistique des catalogues de certains clients.</i></p>
        <ul>
            <li>Préparer les éléments à expedier en fonction des demandes des charges de clientèles. Conditionner les DVD : mise sous boitier.</li>
            <li>Imprimer la jaquette adéquate.</li>
            <li>Joindre la lettre d'accompagnement personnalisée en fonction du destinataire.</li>
            <li>Gérer tous les envois CHRONOPOST.</li>
        </ul>

        <p><i>Réceptionner des marchandises.</i></p>
        <ul>
            <li>Accueillir les différents fournisseurs.</li>
            <li>Controler la marchandise livrée.</li>
            <li>Prévenir les services concernés.</li>
        </ul>

        <p><i>Accueil des coursiers et des clients.</i></p>
        <ul>
            <li>Prendre connaissance des demandes.</li>
            <li>Controler les réceptions.</li>
            <li>Donner les bons éléments en cas d'enlèvement.</li>
            <li>Orienter un client ou prévenir un chargé de clientèle si besoin.</li>
        </ul>

        <p><u>Compétences nécessaires :</u></p>
        <p><i>Culture professionnelle de base.</i></p>
        <ul>
            <li>Culture audiovisuelle sur l'ensemble des formats existants.</li>
        </ul>

        <p><i>Compétences techniques :</i></p>
        <ul>
            <li>Maîtrise de l'outil informatique interne.</li>
            <li>Connaissance du pack Office MICROSOFT.</li>
        </ul>

        <p><i>Compétences personnelles et relationnelles :</i></p>
        <ul>
            <li>Autonomie.</li>
            <li>Rigueur.</li>
            <li>Capacité à prendre des décisions.</li>
            <li>Capacité à travailler en équipe.</li>
            <li>Capacité à communiquer.</li>
        </ul>
