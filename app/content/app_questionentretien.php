        <h2>Les questions les plus souvent posées</h2>

        <p>
            On vous pose une question et vous ne savais pas quoi répondre ? Ne soyez pas pris au dépourvue !
        </p>

        <ul>
            <li>Parlez moi de vous.</li>
            <br />
            <li>Quelles sont vos qualités et vos défauts ?</li>
            <br />
            <li>Avez-vous un permis de conduire ? Avez-vous une voiture ?</li>
            <br />
            <li>Parlez-moi de vos études. Pourquoi avez-vous choisi ces études ? Que vous ont-elles apportées ?</li>
            <br />
            <li>Quelles sont les matières qui vous on le plus ou le moins intéressé ?</li>
            <br />
            <li>Parlez moi de vos stages et/ou de votre expérience professionnelle.</li>
            <br />
            <li>Connaissez-vous notre société ?</li>
            <br />
            <li>Pourquoi avez-vous répondu à notre annonce ? Pourquoi vous nous avez contacté spontanément ?</li>
            <br />
            <li>Qu'est-ce qui vous attire le plus dans cet emploi ?</li>
            <br />
            <li>Que pensez-vous pouvoir nous apporter ?</li>
            <br />
            <li>Vous êtes une femme. Ne pensez-vous pas que cela constitue un handicap dans notre métier ? (enceinte, gestion des enfants...)</li>
            <br />
            <li>Préférez-vous travailler seul(e) ou en équipe ?</li>
            <br />
            <li>Acceptez-vous la hiérarchie ? Comment réagissez-vous vis à vis de la hiérarchie ?</li>
            <br />
            <li>Quand êtes-vous disponible ?</li>
            <br />
            <li>Depuis combien de temps recherchez-vous du travail ?</li>
            <br />
            <li>Quelles sont vos occupations préférées pendant vos loisirs ?</li>
            <br />
            <li>Avez-vous contacté d'autres entreprises ? Avez-vous d'autres propositions ?</li>
            <br />
            <li>Accepteriez-vous de voyager ? (cela peut-être un poste dans une autre régions ou dans un autre pays, sauf si c'est le job).</li>
            <br />
            <li>Quelles sont vos prétentions de salaire ? (je vous rappelle qu'il y a de convention collective ou votre qualification est prise en compte, renseignez-vous avant de venir).</li>
        </ul>

        <p>
            Voila ces quelques questions vous permettrons de bien vous préparer pour votre entretien. Pour les cinq dernières questions c'est au fillingue, mais faite attention à ce que vous direz.
        </p>

    </article>
